=head1 DESCRIPTION

The purpose of this distribution is to provide "helpers" that allow one 
to generate two things from a SPORE API definition.

* An application for one of the supported frameworks.
* An API skeleton (think MyApp.pm with methods that will called from within 
  a chose framework dispatcher/action)
  
The idea is to jump start the development of the API to support the SPORE definition.

=cut


